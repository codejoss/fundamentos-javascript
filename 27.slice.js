// Slice - Sirve para extraer un rango de elementos de un array

const animalArray = ['hormiga', 'oso', 'Pato', 'orca', 'elefant'];

console.log(animalArray.slice(2))
console.log(animalArray.slice(2, 4))
console.log(animalArray.slice(2, 5))
console.log(animalArray.slice(-2))
console.log(animalArray.slice(2, -1))